# string functions

var= 'karachi'
print (var.capitalize()) #capital first letter
print (var.center(20, '*')) #center the string
print (var.count('a')) #count the number of a
print (var.find('b')) #find the index of b (if not found then -1)
print (var.isalpha()) #check if all characters are alphabets
print (var.isdigit()) #check if all characters are digits
print (var.islower()) #check if all characters are lower case
print (var.isupper()) #check if all characters are upper case