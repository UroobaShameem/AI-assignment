# functions of dictionary

dict= {'name': 'ali', 'age': 20, 'city': 'karachi'}
print (dict)
print (dict['name']) #print the value of key name
print (dict.keys()) #print the keys of dictionary
print (dict.values()) #print the values of dictionary
print (dict.items()) #print the items of dictionary
dict.update({'education': 'bachelors'}) #update the dictionary
print (dict)